
//Split
let str = 'pepcoder'

let partsOfString = str.split('c')//return an array
// split function will split a string according to a passed paramerter
// and will return the left and the right part in an array

console.log(partsOfString) // [pep , oder]

//Join

let joinedStr = partsOfString.join('c')
// Join function will take the elements of an array and it will fill the parameter after each element 
//of the array with the passed parameter
// and will return the complete str
console.log(joinedStr) // joined Str (pepcoder)


//OUTPUT
// [ 'pep', 'oder' ]
