//Java is an strongly typed language whereas javascript is an lossly typed/Dynamically typed language , that is we dont need to define the data type before declaring the variable

let a = 'Hello'
console.log(a , typeof(a))

let b = 2.5
console.log(b , typeof(b))

let c = true
console.log(c , typeof(c))

let d = undefined
console.log(d , typeof(d))

let e = null
console.log(e , typeof(e))

// OUTPUT

// Hello string
// 2.5 number
// true booleanundefined undefined
// null object


// Primitive types are:

// Number : 1, 1.5, 0, -1, -1.5, Infinity, -Infinity, NaN
// String : "Hello", 'Hello', "1", '1'
// Boolean : true, false
// Symbol : Symbol()
// Undefined : undefined
// Null : null
// NaN : NaN


// Reference types are:


// Object : {}, new Object()
// Array : [], new Array()
// Function : function() {}


// Date : new Date()
// RegExp : /\w+/
// Error : new Error()
// Map : new Map()
// Set : new Set()
// WeakMap : new WeakMap()
// WeakSet : new WeakSet()  

